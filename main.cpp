#include <iostream>
#include <stack>

using namespace std;

class Brainfuck {
  public:
    char data[30000];
    int d;

    const char *program;
    int progsize;
    int p;

    std::stack<int> loops;

    Brainfuck(const char* prog) {
      d = 0;
      program = prog;
      progsize = strlen(program);
      p = 0;
    }

    void pincr() {
      ++d;
    }

    void pdecr() {
      --d;
    }

    void bincr() {
      ++data[d];
    }

    void bdecr() {
      --data[d];
    }

    void puts() {
      std::cout << data[d];
    }

    void gets() {
      std::cin >> data[d];
    }

    void bropen() {
      int bal = 1;
      if (data[d] == '\0') {
        do {
          p++;
          if      (program[p] == '[') bal++;
          else if (program[p] == ']') bal--;
        } while ( bal != 0 );
      }
    }

    void brclose() {
      int bal = 0;
      do {
        if      (program[p] == '[') bal++;
        else if (program[p] == ']') bal--;
        p--;
      } while ( bal != 0 );
    }

    void evaluate() {
      while (p < progsize) {
        switch (program[p]) {
          case '>':
            pincr();
            break;
          case '<':
            pdecr();
            break;
          case '+':
            bincr();
            break;
          case '-':
            bdecr();
            break;
          case '.':
            puts();
            break;
          case ',':
            gets();
            break;
          case '[':
            bropen();
            break;
          case ']':
            brclose();
            break;
        }
        p++;
      }
    }
};

